def load_model(model, model_path, location=None):
    """
    Loads model from a path
    :param model:
    :param model_path:
    :param location:
    :return: loaded model
    """
    state_dict = torch.load(model_path, map_location=location)

    # Error check one, if state_dict is a key
    if 'state_dict' in state_dict.keys():
        state_dict = state_dict['state_dict']
    # Error check if model was saved as Data parallel
    state_dict = {(k[7:] if k.startswith('module.') else k): v for k, v in state_dict.items()}

    model.load_state_dict(state_dict)
    return model


class MobileNetV1(nn.Module):
    def __init__(self):
        super(MobileNetV1, self).__init__()

        def conv_bn(inp, oup, stride):
            return nn.Sequential(
                nn.Conv2d(inp, oup, 3, stride, 1, bias=False),
                nn.BatchNorm2d(oup),
                nn.ReLU(inplace=True)
            )

        def conv_dw(inp, oup, stride):
            return nn.Sequential(
                nn.Conv2d(inp, inp, 3, stride, 1, groups=inp, bias=False),
                nn.BatchNorm2d(inp),
                nn.ReLU(inplace=True),

                nn.Conv2d(inp, oup, 1, 1, 0, bias=False),
                nn.BatchNorm2d(oup),
                nn.ReLU(inplace=True),
            )

        self.model = nn.Sequential(
            conv_bn(3, 32, 2),
            conv_dw(32, 64, 1),
            conv_dw(64, 128, 2),
            conv_dw(128, 128, 1),
            conv_dw(128, 256, 2),
            conv_dw(256, 256, 1),
            conv_dw(256, 512, 2),
            conv_dw(512, 512, 1),
            conv_dw(512, 512, 1),
            conv_dw(512, 512, 1),
            conv_dw(512, 512, 1),
            conv_dw(512, 512, 1),
            conv_dw(512, 1024, 2),
            conv_dw(1024, 1024, 1),
            nn.AvgPool2d(7),
        )
        self.fc = nn.Linear(1024, 1000)

    def forward(self, x):
        x = self.model(x)
        # The following line has replaced the original x = x.view(-1, 1024)
        # This is required for tensorboardX to correctly interpret
        # this statement as a Flatten operation.
        x = x.view(x.size(0), -1)

        x = self.fc(x)
        return x


