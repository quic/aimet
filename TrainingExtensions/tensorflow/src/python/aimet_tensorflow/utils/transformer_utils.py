""" utils associated with tensorflow transformer quantization handling """

from typing import Set

MASK_OVERRIDE_VALUE = -6

SUPPORTED_ATTENTION_MASK_OVERRIDE = {'attention/self/Add'}


def register_attention_mask_override(attention_mask_name: str):
    """
    Registers attention mask name to be clamped
    :param attention_mask_name: Name of attention mask
    """
    if attention_mask_name not in SUPPORTED_ATTENTION_MASK_OVERRIDE:
        SUPPORTED_ATTENTION_MASK_OVERRIDE.add(attention_mask_name)


def get_supported_attention_mask_name() -> Set[str]:
    """
    Returns set of supported attention mask names
    :return set of supported attention mask names
    """
    return SUPPORTED_ATTENTION_MASK_OVERRIDE
